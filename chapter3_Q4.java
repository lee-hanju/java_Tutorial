import java.util.Scanner;

public class gnd{
    public static void main(String[] args) {
        Scanner s = new Scanner(System.in);
        System.out.print("소문자 알파벳 하나를 입력하시오>> ");
        
       String alp = s.next(); 

        char a = alp.charAt(0);
        for(int i = 0; i <=a-'a'; i++){
            for(char j = 'a'; j<=a-i; j++){
                System.out.print(j);
            }
            System.out.println();
        }
        s.close();
    }
}